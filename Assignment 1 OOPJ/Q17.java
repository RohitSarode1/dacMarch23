class Q17
{
      public static void main(String args[])
      {
        int value = 5555;
        Integer a1 = new Integer(value);

        byte num1 = a1.byteValue();
        System.out.println("num1 :" +num1);

        short num2 = a1.shortValue();
        System.out.println("num2 :" +num2);

        int num3 = a1.intValue();
        System.out.println("num2 :" +num3);

        float num4 = a1.floatValue();
        System.out.println("num2 :" +num4);

        double num5 = a1.doubleValue();
        System.out.println("num2 :" +num5);

      }    
}
